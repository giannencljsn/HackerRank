SinglyLinkedListNode* deleteNode(SinglyLinkedListNode* llist, int position) {
    
    //initialize current node from llist
    SinglyLinkedListNode* curr = llist;
    //pointer 
    int currentIndex = 0;
    
    //while current position is not null and 
    //current Index is not equal to the previous node of the
    //element that needs to be deleted
    
    //case if position points to the header just return the
    //next node of curr since curr points to header by default
    if(position == 0){
        return curr->next;
    }
    while(curr != nullptr && currentIndex != position - 1){
        //traverse until it reaches the previous node before
        //the deleted element
        //ex. A->B->C->D->E and position 3 is to be deleted
        //curr = A->B->C ; curr= C
        curr = curr->next;
        //this increment value keeps it traversing
        currentIndex++; 
        
    }
    //curr = C
    //C->next = D->next
    curr->next = curr->next->next;
    return llist;
}

